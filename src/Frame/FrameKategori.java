private void tampilkanDataKategori() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Kategori");
        model.addColumn("Nama Kategori");
        model.addColumn("Deskripsi");
        
        try {
            String query = "SELECT id_kategori, nama_kategori, deskripsi FROM KATEGORI_BARANG ORDER BY id_kategori";
            ResultSet rs = objekKu.getData(query);
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_kategori"),
                    rs.getString("nama_kategori"),
                    rs.getString("deskripsi")
                });
            }
            tabelKategori.setModel(model);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat memuat data Kategori: " + e.getMessage());
        }
    }

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String nama = txtNamaKategori.getText();
        String deskripsi = txtDeskripsi.getText();

        if (nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Kategori tidak boleh kosong!");
            return;
        }

        // Memanggil method dari class crud (Asumsi: crud.simpanKategori(nama, deskripsi) mengembalikan boolean)
        boolean result = objekKu.simpanKategori(nama, deskripsi);
        
        if (result) {
            JOptionPane.showMessageDialog(this, "Data Kategori berhasil disimpan!");
            clearFields();
            tampilkanDataKategori();
        } else {
             JOptionPane.showMessageDialog(this, "Gagal menyimpan data Kategori.");
    }//GEN-LAST:event_btnSimpanActionPerformed
    }
    
    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
       if (selectedIdKategori == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data di tabel yang akan dihapus.");
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin hapus Kategori ID " + selectedIdKategori + "?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
            
        if (konfirmasi == JOptionPane.YES_OPTION) {
            // Memanggil method dari class crud
            boolean result = objekKu.hapusKategori(selectedIdKategori);
            
            if (result) {
                JOptionPane.showMessageDialog(this, "Data Kategori berhasil dihapus!");
                clearFields();
                tampilkanDataKategori();
            } else {
                 JOptionPane.showMessageDialog(this, "Gagal menghapus data Kategori. Pastikan tidak ada Barang Bukti/Temuan yang terhubung.");
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        if (selectedIdKategori == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data di tabel yang akan diubah.");
        return;
    }
    
    String nama = txtNamaKategori.getText();
    String deskripsi = txtDeskripsi.getText();

    if (nama.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nama Kategori tidak boleh kosong!");
        return;
    }
    
    // Perbaikan di sini: Pastikan idKategori adalah Integer/String sesuai method crud
    // Kita asumsikan selectedIdKategori bertipe int
    boolean result = objekKu.ubahKategori(selectedIdKategori, nama, deskripsi); // <-- HARUS SESUAI DENGAN CRUD
    
    if (result) {
        JOptionPane.showMessageDialog(this, "Data Kategori berhasil diubah!");
        clearFields();
        tampilkanDataKategori();
    } else {
         JOptionPane.showMessageDialog(this, "Gagal mengubah data Kategori.");
    }
    }//GEN-LAST:event_btnUbahActionPerformed


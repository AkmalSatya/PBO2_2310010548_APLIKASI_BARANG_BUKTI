    private void btnHapusLogTerpilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusLogTerpilihActionPerformed
      if (selectedIdLog == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris log yang akan dihapus.");
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin hapus Log ID " + selectedIdLog + "?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
            
        if (konfirmasi == JOptionPane.YES_OPTION) {
            // Memanggil method dari class crud
            // Asumsi: crud.hapusLogAktivitas(int id) mengembalikan boolean
            boolean result = objekKu.hapusLogAktivitas(selectedIdLog);
            
            if (result) {
                JOptionPane.showMessageDialog(this, "Log berhasil dihapus!");
                tampilkanDataLog();
                clearSelection();
            } else {
                 JOptionPane.showMessageDialog(this, "Gagal menghapus Log.");
            }
        }
    }//GEN-LAST:event_btnHapusLogTerpilihActionPerformed

    private void btnHapusLogTerpilihMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnHapusLogTerpilihMouseClicked
        int baris = tabelLog.getSelectedRow();
        if (baris != -1) {
            // ID Log ada di kolom 0 (sesuai DefaultTableModel)
            selectedIdLog = (int) tabelLog.getValueAt(baris, 0); 
            btnHapusLogTerpilih.setEnabled(true);
        }
    }//GEN-LAST:event_btnHapusLogTerpilihMouseClicked


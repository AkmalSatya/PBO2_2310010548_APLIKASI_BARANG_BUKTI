private void tampilkanDataLog() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Log");
        model.addColumn("ID User");
        model.addColumn("Waktu Aksi");
        model.addColumn("Jenis Aksi");
        model.addColumn("Deskripsi");
        
        try {
            // Query JOIN untuk menampilkan Username/Nama Pegawai (jika diperlukan)
            String query = "SELECT l.id_log, l.id_user, l.waktu_aksi, l.jenis_aksi, l.deskripsi FROM LOG_AKTIVITAS l ORDER BY l.waktu_aksi DESC";
            ResultSet rs = objekKu.getData(query);
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_log"),
                    rs.getInt("id_user"),
                    rs.getTimestamp("waktu_aksi"),
                    rs.getString("jenis_aksi"),
                    rs.getString("deskripsi")
                });
            }
            tabelLog.setModel(model);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat memuat data Log: " + e.getMessage());
        }
    }
private void clearSelection() {
        selectedIdLog = -1;
        btnHapusLogTerpilih.setEnabled(false);
        tabelLog.clearSelection();
}

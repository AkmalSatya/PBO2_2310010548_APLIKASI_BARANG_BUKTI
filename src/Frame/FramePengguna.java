private void tampilkanDataPengguna(String keyword) {
        DefaultTableModel model = new DefaultTableModel();
        // Kolom disesuaikan agar cocok dengan gambar user (ID Anggota, Nama, Status, Telp)
        model.addColumn("ID Anggota"); 
        model.addColumn("Nama");       
        model.addColumn("Status");     
        model.addColumn("Telp");       
        
        try {
            // Asumsi: Tabel di DB bernama PENGGUNA. Kolom ID=id_user, Nama=nama_pegawai, Status=jabatan, Telp=username
            String query = "SELECT id_user, nama_pegawai, jabatan, username " +
                           "FROM PENGGUNA " +
                           "WHERE nama_pegawai LIKE '%" + keyword + "%' OR " +
                           "id_user LIKE '%" + keyword + "%' " +
                           "ORDER BY id_user";
            
            ResultSet rs = objekKu.getData(query);
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_user"),
                    rs.getString("nama_pegawai"),
                    rs.getString("jabatan"),
                    rs.getString("username") // Menggunakan username sebagai data Telp sementara
                });
            }
            tabelPengguna.setModel(model); 
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat memuat data: " + e.getMessage());
        }
    }

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
       try {
            String id = txtIDUser.getText();
            String user = txtUsername.getText();
            String pass = String.valueOf(txtPassword.getPassword()); // Ambil dari PasswordField
            String nama = txtNamaPegawai.getText();
            String jabatan = txtJabatan.getText();
            
            if(id.isEmpty() || user.isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID dan Username tidak boleh kosong!");
                return;
            }
            objekKu.simpanPengguna(id, user, pass, nama, jabatan);
            tampilkanDataPengguna("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error saat menambah data: " + e.getMessage());
        }
    
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        try {
            String id = txtIDUser.getText();
            String user = txtUsername.getText();
            String pass = String.valueOf(txtPassword.getPassword());
            String nama = txtNamaPegawai.getText();
            String jabatan = txtJabatan.getText();
            
            if(id.isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID User harus diisi untuk mengubah data!");
                return;
            }
            objekKu.ubahPengguna(id, user, pass, nama, jabatan);
            tampilkanDataPengguna("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error saat mengubah data: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try {
            String id = txtIDUser.getText();
            if(id.isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID User harus diisi untuk menghapus data!");
                return;
            }
            
            int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin hapus data Pengguna ID " + id + "?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            
            if (konfirmasi == JOptionPane.YES_OPTION) {
                 objekKu.hapusPengguna(id);
                 tampilkanDataPengguna("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error saat menghapus data: " + e.getMessage());
        }
    
    }//GEN-LAST:event_btnHapusActionPerformed

